from __future__ import print_function

import sys

class colors:
    HEADER = '\033[95m'
    OKBLUE = '\033[94m'
    OKGREEN = '\033[92m'
    WARNING = '\033[93m'
    FAIL = '\033[91m'
    ENDC = '\033[0m'
    BOLD = '\033[1m'
    UNDERLINE = '\033[4m'


def log(text, color=None):
    if sys.stdout.isatty():
        if color:
            print(color, text)
        else:
            print(colors.ENDC, text)
    else:
        print(text)
