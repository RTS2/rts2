void ImageReq::execute (XmlRpc::XmlRpcSource *source, struct sockaddr_in *saddr, std::string path, XmlRpc::HttpParams *params, int &http_code, const char* &response_type, char* &response, size_t &response_length)
{
	const image_t *selImage = NULL;
	if (path == "/up.png")
	{
		selImage = &up;
	}
	else
	{
		throw XmlRpc::XmlRpcException ("cannot find image " + path);
	}

	http_code = HTTP_OK;

	cacheMaxAge (CACHE_MAX_STATIC);

	response_type = "image/png";
	response_length = selImage->len;
	response = new char[response_length];
	memcpy (response, selImage->data, response_length);
}
